<h1>About this Site</h1>
<p>
This website aims to help people who meditate find a more comfortable posture. I am not an expert on the subject of meditation posture but an experienced student who would like to help others. 
</p>

<p>
If you are just begining meditation, I hope that the information offered here will help you to circumvent some of the common problems that beginners often encounter. If you are a seasoned practitioner, I hope you might learn a new way to sit, or simply find the information here interesting. If you are a meditation teacher, or an expert on posture, then I would welcome your feedback or suggestions on how this site may be improved. 
</p>

<p>
If you are new to meditation, I would also like to take this opportunity to congratulate you on taking the first steps on an extraordinary path. There are many benefits to meditation, too many to describe a few short paragraphs. Do not be deterred by your preliminary troubles. Stay determined and practice every day. Soon you will be on a wonderful journey. It is well worth both the time and the effort.
</p>
