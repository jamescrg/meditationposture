<h1>Positioning the Head</h1>

<p>The position of the head is highly important, and it can have a significant effect on your comfort.</p>

<h2>Keep the Chin Tucked</h2>

<p>The head should be held up straight with the back of the neck long. This means that the chin will be gently tucked, like the picture below.</p>

<p>Once you have the head correctly positioned, it is important to avoid three potential problems: drifting, falling, and straining.</p>

<h2>Drifting</h2>

<p>Drifting occurs when the head when the head starts moving slowly forward with the chin sticking out. This one of the most common problems people have. Allowing the head to drift can become very uncomfortable over time. It causes the muscles at the base of the skull to contract, and it shifts some of the weight of the head to the mid back, which will become tense. The best way to prevent drifting is to form the habit of keeping the chin tucked.</p> 

