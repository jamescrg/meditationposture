<h2>Falling</h2>

<p>In keeping the chin tucked, you also want to be careful not to allow the head from "falling" forward. This will also transfer tension the mid back. If the head is falling forward, you may just need to make a conscious effort to hold it up, until doing so becomes a habit. You also may need to lean back just a little bit. If the torso torso is leaned forward the head will have a tendency to fall forward, as well.</p> 

