<h1>Common Questions</h1>
<p>How far back to sit on the cushion</p>
<p>Should the back be straight?</p>
<p>The back should be straight.  This means no slumping.  Pelvis tilted forward, chest relaxed, shoulders rotated back and relaxed.</p>
<p>Should the spine be straight like a stack of coins?</p>
<p>At one time, this was a useful metaphor for teaching people to sit with a straight back.  Today, it is confusing, because the anatomy of the spine is so widely understood, and the vertebrae do not sit straight.</p>
<p>Eyes open or closed?</p>
<p>Should I retract my shoulders?</p>
<p>Should my weight be over my sitz bones?</p>
<p>This can cause over-arching.  Might work well on a chair or bench.  Confusing on a zafu.</p>
