<h1>Refresher</h1>

<p>For those who want a quick refresher, here is a short list presenting the fundamentals of good posture:</p>


<ol id="refresher">

    <li>Knees below hips</li>

    <li>Pelvis tilted forward</li>

    <li>Center of balance over sitz bones</li>

    <li>Hands in lap</li>

    <li>Chest in a neutral position and relaxed</li>

    <li>Shoulders rolled back and relaxed</li>

    <li>Chin slightly down, back of the neck long</li>

    <li>Relax</li>

</ol>

