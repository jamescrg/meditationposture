<h3>Pain in the hip flexors</h3>
<p>Get the knees below the hips</p>
<p>Lean more forward</p>
<p><b>Towels under zafu</b></p>
<h2>Pain between the shoulder blades</h2>
<p>Arm placement</p>
<p>Rounding the shoulders</p>
<p>Insufficient pelvic tilt</p>
<h2>Pain in the middle of the back</h2>
<p>Lower the chest</p>
<p>Increase pelvic tilt</p>
<h2>Pain in the lower back</h2>
<p>More pelvic tilt</p>
<p>Too much pelvic tilt</p>
<p>Lower the chest</p>
<h2>Residual pain in the lower back</h2>
<p>Too much pelvic tilt</p>
<p>Need to acclimate to pelvic tilt</p>
<h2>Legs falling asleep</h2>
