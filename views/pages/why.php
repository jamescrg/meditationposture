<h1>The Purpose of Sitting</h1>

<p>A person can meditate in almost any position: standing, reclining, lying down, walking, or even while doing yoga. Nevertheless, there is a case to be made for sitting.</p>

<h2>Sitting is Helpful</h2>

<p>
Sitting up with good posture, without back support, is arguably the best physical position for meditation. Although sitting with perfect posture is not necessary, there are many benefits to sitting upright without a back rest. Sitting without a rest for the back, when done comfortably, is both active and relaxing. It allows the body to settle into a restful position while helping to keep the mind alert. In addition, sitting upright with good posture is comfortable for long periods of time.
</p>

<blockquote>
A balanced posture requires very little effort to sustain and allows the major muscles of the body to relax. This relatively small expenditure of energy, coupled with the phenomenon of relaxation, produces a distinct feeling tone of softness, ease, and vibratory flow. It also generates a natural condition of alert awareness. This dual condition of comfort in the body and relaxed alertness in the mind is the fruit of balance. (Johnson 20.)
</blockquote>

<p>In the Zen tradition of Buddhism, posture is considered to be of central importance. Perhaps this is what lead Zen master Shunryu Suzuki to say the following:</p>

<blockquote>
These forms are not a means of obtaining the right state of mind. To take this posture itself is the purpose of our practice. When you have this posture, you have the right state of mind, so there is no need to attain some special state. (Suzuki 9.)
</blockquote>

<h2>Sitting is not Required</h2>

<p>Although sitting upright is very helpful, not every person has the option of sitting without back support. Sitting in a chair may be the only alternative for a person with an injury or disability. Nevertheless, a person can meditate reclining in a soft chair, laying down on a mattress, or even walking outdoors. A physical limitation like an injury should not dampen one's enthusiasm for taking up the practice of meditation. </p>

<blockquote>
All these suggestions about leg positions should be taken as guidelines, not as rules. Do the best you can, and no more will be asked. One of our members at the Maui Zendo did a full seven-day sesshin flat on her back. She had ruptured a disk, and could not even sit up without assistance. Daito Kokushi, great master of early Japanese Zen . . . could not sit in any of the conventional ways. (Aitken 20.)
</blockquote>

<p>Also, it is my understanding that Tibetan monks sit on the floor without the benefit of a cushion, and typically have a relaxed, even slumped posture, at least relative to what is described in this guide. This suggests to me that success in meditation is less about posture and more about attitude and dedication. Nevertheless, because sitting is helpful, it is worth learning how to sit in the healthiest and most comfortable manner possible.</p>

<h2>Other Benefits of Sitting Posture</h2>

<p>
As a final note on the benefits of posture, I should add that learning to become comfortable while sitting in meditation will pay dividends in other areas of life. You will be also be more comfortable everywhere else, whether sitting on a stool or bench, standing for long periods of time, walking, running, or working in the kitchen. Learning posture may seem confusing or difficult at first, but it is well worth persisting through the early difficulties. Once you find a comfortable alignment, good posture will serve to enhance your overall quality of life. 
</p>


